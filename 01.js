// С помощью цикла while найдите сумму чисел от 1 до 20
// Результат присвойте переменной sum

let sum = 0;     // Переменная для хранения суммы
let i = 1;       // Счётчик, начинаем с 1

while (i <= 20) { // Условие: пока i меньше или равно 20
    sum = sum + i; // Добавляем текущее i к сумме
    i = i + 1;     // Увеличиваем счётчик на 1
}

console.log(sum); // Выведет: 210